public class EvalType {
    protected int x3;
    protected int x2;
    protected int x1;
    protected int o3;
    protected int o2;
    protected int o1;

    public EvalType() {
        this.x3 = 0;
        this.x2 = 0;
        this.x1 = 0;
        this.o3 = 0;
        this.o2 = 0;
        this.o1 = 0;
    }

    public void printEvalType() {
        System.out.println("x3 = " + x3);
        System.out.println("x2 = " + x2);
        System.out.println("x1 = " + x1);
        System.out.println("o3 = " + o3);
        System.out.println("o2 = " + o2);
        System.out.println("o1 = " + o1);
    }

}
